package com.example.fauziw97.ponapp.data.model;

public class Schedule {
    private String sportsName;
    private String sportsImage;
    private String timeA;
    private String timeB;
    private String timeC;
    private String date;

    public Schedule() {

    }

    public String getSportsName() {
        return sportsName;
    }

    public void setSportsName(String sportsName) {
        this.sportsName = sportsName;
    }

    public String getSportsImage() {
        return sportsImage;
    }

    public void setSportsImage(String sportsImage) {
        this.sportsImage = sportsImage;
    }

    public String getTimeA() {
        return timeA;
    }

    public void setTimeA(String timeA) {
        this.timeA = timeA;
    }

    public String getTimeB() {
        return timeB;
    }

    public void setTimeB(String timeB) {
        this.timeB = timeB;
    }

    public String getTimeC() {
        return timeC;
    }

    public void setTimeC(String timeC) {
        this.timeC = timeC;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
