package com.example.fauziw97.ponapp.data.model;

public class Athletes {
    private String athletesImage;
    private String athletesName;
    private String athletesContingent;
    private String athletesSex;
    private String athletesSports;
    private int athletesAge;

    public Athletes() {

    }

    public String getAthletesImage() {
        return athletesImage;
    }

    public void setAthletesImage(String athletesImage) {
        this.athletesImage = athletesImage;
    }

    public String getAthletesName() {
        return athletesName;
    }

    public void setAthletesName(String athletesName) {
        this.athletesName = athletesName;
    }

    public String getAthletesContingent() {
        return athletesContingent;
    }

    public void setAthletesContingent(String athletesContingent) {
        this.athletesContingent = athletesContingent;
    }

    public String getAthletesSex() {
        return athletesSex;
    }

    public void setAthletesSex(String athletesSex) {
        this.athletesSex = athletesSex;
    }

    public String getAthletesSports() {
        return athletesSports;
    }

    public void setAthletesSports(String athletesSports) {
        this.athletesSports = athletesSports;
    }

    public int getAthletesAge() {
        return athletesAge;
    }

    public void setAthletesAge(int athletesAge) {
        this.athletesAge = athletesAge;
    }
}
